def proctition(arr, &prc)
    new_arr = []
    first = []
    second = []
    arr.each do |ele|
        if prc.call(ele)
            first.push(ele)
        else
            second.push(ele)
        end
    end
    new_arr = first + second
end

p proctition([4, -5, 7, -10, -2, 1, 3]) { |el| el > 0 }
# [4, 7, 1, 3, -5, -10, -2]
# debugger
